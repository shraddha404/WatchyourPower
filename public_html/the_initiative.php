	<p  align="JUSTIFY">Electricity consumers in India often face poor quality supply in the form of frequent interruptions, load shedding / blackouts, and low voltage levels. This forces them to invest heavily in alternative power supply sources and voltage stabilizing devices or suffer inconvenience and loss of productivity. It is estimated that consumers spend thousands of crore rupees every year to overcome poor supply quality. The problem is more severe in rural and semi-urban areas. The electric utilities continue to justify large capital expenditure and consequent tariff increase by citing the goal of improvement in supply quality. But there is no independent mechanism to monitor the supply quality and hold electric utilities accountable for their performance. Through ESMI we want to provide <b>evidence based feedback</b> about the quality of supply to all stakeholders in the electricity sector, including distribution companies, regulatory commissions, government agencies, civil society organisations, and consumer groups.  We hope that this will lead to improvement in the quality of supply and fairness in distribution of electricity in India.</p>
<p  align="JUSTIFY">We want to develop a reliable database on supply interruptions and voltage levels at consumer locations in India. We plan to install hundreds of Electricity Supply Monitors (ESM) in households, farms, and small commercial establishments all over India. The ESM is a plug-in device that integrates a voltage recorder and a data-logger and can be easily installed at any remote location without the services of an electrician. The ESM records voltage by the minute at its location and sends the data to a central server using GPRS, which is made available on this website.</p>
<p  align="JUSTIFY">Consumers, civil society organizations, researchers, regulatory commissions and other concerned actors are encouraged to use this data to increase accountability of electric utilities. Electricity supply quality can be compared to the standards of performance prescribed by regulatory commissions. The utilities’ capital expenditure on improvement in supply quality can be scrutinized by verifying whether the supply quality has actually improved. The data can be used to verify whether the villages electrified through India’s rural electrification program get the mandated 6 hours of daily supply. The supply quality in different regions can be compared to underscore biases, if any, in supply interruptions and quality.</p>


<p  align="JUSTIFY">"Electricity Supply Monitoring Initiative (ESMI) was selected as a finalist for the Google Impact Challenge, India, 2013 given to NGOs using technology for social impact.”</p>

<p  align="JUSTIFY">One study shows that every additional hour of access leads to one percentage point increase in income of non-farming enterprises (NFE) in India. There are also societal benefits from better access to electricity such as improved health and better education which have not been quantified. The project will also improve system planning in the long run by ensuring that utilities adhere to standards of performance. Improving the quality of supply would greatly reduce damages to appliances and equipment, especially agricultural pumps and transformers. </p>	 
